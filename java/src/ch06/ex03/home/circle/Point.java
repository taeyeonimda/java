package ch06.ex03.home.circle;

public class Point {
	private int x;
	private int y;
	
	public Point() {
		this.x = x;
		this.y = y;
		
	}
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
